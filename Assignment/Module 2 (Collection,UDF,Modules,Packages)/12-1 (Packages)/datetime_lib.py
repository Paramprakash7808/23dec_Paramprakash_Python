import datetime

x = datetime.datetime.now()
print("Current Date Time is:",x)

print("Current Day is:",x.day)
print("Current Month is:",x.month)
print("Current Year is:",x.year)
print("Current Second is:",x.second)
print("Current Micro-Second is:",x.microsecond)
print("Current Minute is:",x.minute)
print("Current Hour is:",x.hour)