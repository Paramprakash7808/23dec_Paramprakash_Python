# Write a Python program that manipulates and prints strings using various string methods.

mystr = "This is Python!"
print(mystr)
print('Strip Method:',mystr.strip())
print('Upper Method:',mystr.upper())
print('Lower Method:',mystr.lower())
print('Replace Method:',mystr.replace('i','I'))
print('Split Method:',mystr.split())
print('Capitalize Method:',mystr.capitalize())
print('Casefold Method:',mystr.casefold())
print('Title Method:',mystr.title())